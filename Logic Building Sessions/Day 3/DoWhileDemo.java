public class DoWhileDemo {
    public static void main(String[] args) {
        do{
            System.out.println(" DoWhileDemo");
        } while(false);
        System.out.println();

        do{
            System.out.println(" DoWhileDemo");
        } while(false);  
        System.out.println(10);
        System.out.println();

        int a=10;
        do{
            System.out.println(" DoWhileDemo");
            a--;
        }while(a>=5);
        System.out.println(10);
        System.out.println();
    }
}
