class Q6 {
    public static void main(String[] args) {
        //Declaring results
        int a = 125, b = 24;
        //addition
        System.out.println(a + " + " + b + " = " + (a+b));
        //subtraction
        System.out.println(a + " - " + b + " = " + (a-b));
        //multiplication
        System.out.println(a + " x " + b + " = " + (a*b));
        //Division
        System.out.println(a + " / " + b + " = " + (a/b));
        //Remainder (Modulo operator)
        System.out.println(a + " mod " + b + " = " + (a%b));
    }   
}
